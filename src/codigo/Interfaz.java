    private void btn_abrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_abrirActionPerformed
        
        
        File archivofc;
        FileReader fr;
        BufferedReader br;
        String line;
        String texto ="";
   
         
        
        try{
            JFileChooser abrir= new JFileChooser();
            abrir.showOpenDialog(null);
            archivofc=abrir.getSelectedFile();
            //System.out.println(archivofc);
            fr= new FileReader(archivofc);
            br= new BufferedReader(fr);
            
            while((line=br.readLine())!=null){
                //System.out.println(line);
                texto+=line + "\n";
            }
            
            br.close();
            fr.close();
            
            
            
            
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Hubo un error leyendo el archivo"+e);
 
        }
        
        TextAreaConsola.setText(texto);
        System.out.println(texto);
        
    }//GEN-LAST:event_btn_abrirActionPerformed

